/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode next;
 * }
 *
 */
function insertNodeAtHead(head, data) {
  const newNode = new SinglyLinkedListNode(data);

  if (!head) return newNode;

  newNode.next = head;

  return newNode;
}

